package tut.d6classvar.demo01;

public class Weapon {

	private String code;

	public Weapon() {
		super();
	}
	
	public Weapon(String code) {
		super();
		this.code = code;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}
}
